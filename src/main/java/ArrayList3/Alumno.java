public class Alumno {

    private String nombre;
    private double calificacion1;
    private double calificacion2;
    private double calificacion3;

    // Constructor en blanco
    public Alumno() {
    }

    // Constructor completo
    public Alumno(String nombre, double calificacion1, double calificacion2, double calificacion3) {
        this.nombre = nombre;
        this.calificacion1 = calificacion1;
        this.calificacion2 = calificacion2;
        this.calificacion3 = calificacion3;
    }

    // Getters y Setters
    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public double getCalificacion1() {
        return calificacion1;
    }

    public void setCalificacion1(double calificacion1) {
        this.calificacion1 = calificacion1;
    }

    public double getCalificacion2() {
        return calificacion2;
    }

    public void setCalificacion2(double calificacion2) {
        this.calificacion2 = calificacion2;
    }

    public double getCalificacion3() {
        return calificacion3;
    }

    public void setCalificacion3(double calificacion3) {
        this.calificacion3 = calificacion3;
    }

    @Override
    public String toString() {
        return "Alumno{"
                + "nombre='" + nombre + '\''
                + ", calificacion1=" + calificacion1
                + ", calificacion2=" + calificacion2
                + ", calificacion3=" + calificacion3
                + '}';
    }
}
